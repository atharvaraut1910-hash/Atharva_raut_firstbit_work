###Logical operators
#1. and :Return True if both operands are true,otherwise False
print(True and True)
print(True and True)

#2.or:Return False if both operands are False,otherwise True
print(False or False)
print(False or True)

#3. not (Return opposite result)
print(not True)
print(not False)

