###Comparision / Relational
#1. == (exact equals)
print(10 == '10')
print(20 == 20)

#2. !=(Not Equals To)
print(10 != '10')
print(20 != 20)

#3. >(greater than)
print(10>20)
print(20>10)

#4.>=(greater than or equals to)
print(20>=10)
print(20>=20)

#5.<(less than)
print(20<10)
print(10<20)

#6.<=(less than or equals to)
print(20<=10)
print(20<=20)  

