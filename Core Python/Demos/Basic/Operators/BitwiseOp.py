###Bitwise op
#1. &(AND) :if both bits are 1 then consider 1 otherwise 0
print(12 & 6)

#2.|(OR) :if both bits are 0 then cosinder 0 otherwise 1
print(12 | 6)

#3.^(XOR) :if both bits are different then consider 1 otherwise(same)
print(12 ^ 6)

#4.~(NOT) :1's complement
print(~10) # -(10 + 1): -(x + 1)

#5.>>(right shift): remove given numbers of bits from right
print(22 >> 2)

#6.(left shift):add 0 till given numbers to right
print(6 << 3)